package com.samsung;

public class Dog {
    private int age;
    private String name;
    private int weight;

    public Dog(int age, String name, int weight) {
        this.age = age;
        this.name = name;
        this.weight = weight;
    }

    public int getAge() {
        return age;
    }

    public int getWeight() {
        return weight;
    }

    public String getName() {
        return name;
    }
}
